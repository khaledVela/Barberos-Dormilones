public class Barbero {
    boolean duerme = true;
    boolean corta = false;

    public boolean isDuerme() {
        return duerme;
    }

    public void setDuerme(boolean duerme) {
        this.duerme = duerme;
    }

    public boolean isCorta() {
        return corta;
    }

    public void setCorta(boolean corta) {
        this.corta = corta;
    }
}
